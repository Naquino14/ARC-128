        internal static void Permutate(ref byte[] io, in byte[] table, int seed =  1)
        {
            seed = (seed == 0) ? 1 : seed;
            for (int i = 0; i <= io.Length - 1; i++)
                io[i] = (byte)((io[i] * seed % 256) ^ table[i]);
